my_list = [1, 2, 3, 4, 5]

# Append method
my_list.append(6)
print("List after appending 6:", my_list)

# Remove method
my_list.remove(3)
print("List after removing 3:", my_list)

# Extend method
my_list.extend([7, 8, 9])
print("List after extending with [7, 8, 9]:", my_list)

# Sort method
my_list.sort()
print("Sorted list:", my_list)
