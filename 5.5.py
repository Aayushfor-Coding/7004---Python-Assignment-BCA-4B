my_list = [1, 2, 3, 4, 5]

# Length of the list
print("Length of the list:", len(my_list))

# Maximum element
print("Maximum element of the list:", max(my_list))

# Minimum element
print("Minimum element of the list:", min(my_list))

# Sum of elements
print("Sum of elements of the list:", sum(my_list))
